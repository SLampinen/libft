void *ft_memchr(const void *str, int c, int n)
{
    char d;
    char *rv;
    char *s;

    s = (char *)str;
    rv = (void *)0;
    d = c;
    while (n > 0)
    {
        if (*s == d)
            return(s);
        s++;
        n--;
    }
    return(rv);
}
